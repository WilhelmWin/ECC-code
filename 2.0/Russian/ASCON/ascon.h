#ifndef ASCON_H_
#define ASCON_H_

#include <stdint.h>

// =====================================================================
// Структура ascon_state_t:
// - Эта структура хранит внутреннее состояние шифра ASCON.
//   Она использует массив из 5 переменных типа uint64_t для представления состояния,
//   которое обрабатывается в процессе шифрования и расшифровки.
// =====================================================================
typedef struct {
  uint64_t x[5];  // 5 x 64-битных переменных состояния для хранения состояния шифра ASCON
} ascon_state_t;

// =====================================================================
// Объявления функций для шифрования и расшифровки AEAD
// =====================================================================

// Функция шифрования AEAD
int crypto_aead_encrypt(
  unsigned char *c,              // Выходной ciphertext (зашифрованное сообщение)
  unsigned long long *clen,      // Длина ciphertext (выходное значение)
  const unsigned char *m,        // Входное сообщение (plaintext)
  unsigned long long mlen,       // Длина сообщения (plaintext)
  const unsigned char *npub,     // Публичный nonce (обычно используется для уникальности)
  const unsigned char *k         // Ключ (секретный ключ, используемый для шифрования)
);

// =====================================================================
// Функция расшифровки AEAD
// =====================================================================
int crypto_aead_decrypt(
  unsigned char *m,              // Выходное сообщение (расшифрованное сообщение)
  unsigned long long *mlen,      // Длина расшифрованного сообщения (выходное значение)
  unsigned char *nsec,           // Секретный nonce (может быть установлен в null, если не используется)
  const unsigned char *c,        // Входной ciphertext (зашифрованное сообщение)
  unsigned long long clen,       // Длина ciphertext
  const unsigned char *npub,     // Публичный nonce (такой же, как при шифровании)
  const unsigned char *k         // Ключ (такой же, как при шифровании)
);

#endif /* ASCON_H_ */
