#ifndef ASCON_H_
#define ASCON_H_

#include <stdint.h>

// =====================================================================
// Структура ascon_state_t:
// - Эта структура хранит внутреннее состояние шифра ASCON.
//   Она использует массив из 5 переменных типа uint64_t для
//   представления состояния, которое обрабатывается при шифровании и
//   расшифровке.
// =====================================================================
typedef struct {
  uint64_t x[5];  // 5 переменных по 64 бита для хранения состояния
                  // шифра ASCON
} ascon_state_t;

// =====================================================================
// Объявления функций для AEAD шифрования и расшифровки
// =====================================================================

// Функция AEAD шифрования
int crypto_aead_encrypt(
  uint8_t *c,               // Выходной шифротекст
  uint64_t *clen,           // Длина шифротекста (выход)
  const uint8_t *m,         // Входящее сообщение (открытый текст)
  uint64_t mlen,            // Длина сообщения (открытый текст)
  const uint8_t *npub,      // Публичный nonce
  const uint8_t *k          // Ключ (секретный ключ для шифрования)
);

// =====================================================================
// Функция AEAD расшифровки
// =====================================================================
int crypto_aead_decrypt(
  uint8_t *m,              // Выходное сообщение
  uint64_t *mlen,          // Длина расшифрованного сообщения (выход)
  uint8_t *nsec,           // Секретный nonce (может быть NULL,
                           // если не используется)
  const uint8_t *c,        // Входной шифротекст
  uint64_t clen,           // Длина шифротекста
  const uint8_t *npub,     // Публичный nonce
  const uint8_t *k         // Ключ (тот же, что и при шифровании)
);

#endif /* ASCON_H_ */

